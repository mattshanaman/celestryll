# Check for Duplicate Keys in Translation Scripts

Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?     Duplicate Key Checker - All Translation Scripts         ?" -ForegroundColor Cyan
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

$scripts = @(
    "apply-spanish-translations.ps1"
    "apply-german-translations.ps1"
    "apply-french-translations.ps1"
    "apply-japanese-translations.ps1"
    "apply-hindi-translations.ps1"
    "apply-chinese-translations.ps1"
    "apply-arabic-translations.ps1"
)

$totalDuplicates = 0

foreach ($script in $scripts) {
    if (-not (Test-Path $script)) {
        Write-Host "??  $script not found" -ForegroundColor Yellow
        continue
    }
    
    Write-Host "Checking: $script" -ForegroundColor Cyan
    
    $content = Get-Content $script -Raw
    
    # Extract all keys from the hashtable
    $pattern = '^\s*"([^"]+)"\s*='
    $keys = @()
    
    foreach ($line in (Get-Content $script)) {
        if ($line -match $pattern) {
            $keys += $matches[1]
        }
    }
    
    # Find duplicates
    $duplicates = $keys | Group-Object | Where-Object { $_.Count -gt 1 }
    
    if ($duplicates) {
        Write-Host "  ? Found $($duplicates.Count) duplicate key(s):" -ForegroundColor Red
        foreach ($dup in $duplicates) {
            Write-Host "     • $($dup.Name) (appears $($dup.Count) times)" -ForegroundColor Yellow
            $totalDuplicates++
        }
    }
    else {
        Write-Host "  ? No duplicates found" -ForegroundColor Green
    }
    
    Write-Host ""
}

Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?                        SUMMARY                               ?" -ForegroundColor Cyan
Write-Host "????????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

if ($totalDuplicates -eq 0) {
    Write-Host "? ALL SCRIPTS CLEAN - No duplicate keys found!" -ForegroundColor Green
}
else {
    Write-Host "? Found $totalDuplicates duplicate key(s) across all scripts" -ForegroundColor Red
    Write-Host ""
    Write-Host "Please fix duplicates before running apply-all-translations.ps1" -ForegroundColor Yellow
}

Write-Host ""
Write-Host "Press any key to continue..."
$null = $Host.UI.RawUI.ReadKey("NoEcho,IncludeKeyDown")
