$translatedCount = 0
$updatedCount = 0

foreach ($key in $translations.Keys) {
    $arabic = $translations[$key]
    $pattern = "(<data name=`"$key`"[^>]*>[\s\S]*?<value>)([^<]*)(</value>)"
    
    if ($content -match $pattern) {
        $oldValue = $matches[2]
        if ($oldValue -ne $arabic) {
            $content = $content -replace $pattern, "`$1$arabic`$3"
            if ($oldValue -match "^[A-Z]") {
                $translatedCount++
                Write-Host "? $key" -ForegroundColor Green
            } else {
                $updatedCount++
                Write-Host "? $key (updated)" -ForegroundColor Cyan
            }
        }
    }
    else {
        Write-Host "? $key (not found)" -ForegroundColor Yellow
    }
}

