Write-Host "Applying $($translations.Count) translations..." -ForegroundColor Yellow
Write-Host ""

$translatedCount = 0
$updatedCount = 0

foreach ($key in $translations.Keys) {
    $hindi = $translations[$key]
    $pattern = "(<data name=`"$key`"[^>]*>[\s\S]*?<value>)([^<]*)(</value>)"
    
    if ($content -match $pattern) {
        $oldValue = $matches[2]
        if ($oldValue -ne $hindi) {
            $content = $content -replace $pattern, "`$1$hindi`$3"
            if ($oldValue -match "^[A-Z]") {
                $translatedCount++
                Write-Host "? $key" -ForegroundColor Green
            } else {
                $updatedCount++
                Write-Host "? $key (updated)" -ForegroundColor Cyan
            }
        }
    }
    else {
        Write-Host "? $key (not found)" -ForegroundColor Yellow
    }
}

